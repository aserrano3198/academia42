int		ft_isalpha(int c)
{
	int	answer;

	answer = 0;
	if ((c < 'A') || (c < 'a' && c > 'Z'))	
	{
		answer = 1;
	}
	if (c > 'z')
	{
		answer = 1;
	}
	return (answer);
}
