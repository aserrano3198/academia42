void	*memset(void *b, int c, size_t len)
{
	char *bcpy;
	int i;

	i = 0;
	if (!(bccpy = (char *)b))
		return (b);
	while (i < (int)len)
	{
		bcpy[i] = c;
		i++;
	}
	return (b);
}
